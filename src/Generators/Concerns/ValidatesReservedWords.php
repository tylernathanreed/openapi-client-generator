        '__DIR__',
        '__FILE__',
        '__FUNCTION__',
        '__LINE__',
        '__METHOD__',
        '__NAMESPACE__',
        '__TRAIT__',
    ];

    protected function isReservedName(?string $name): bool
    {
        return in_array(strtolower($name), array_map('strtolower', $this->reservedNames));
    }
}
